# حلقة خارجية بتمثل الصفوف (8 صفوف)
for i in range(8):

    # حلقة داخلية بتمثل الأعمدة (8 أعمدة)
    for j in range(8):

        # لو مجموع رقم الصف + رقم العمود عدد زوجي
        if (i + j) % 2 == 0:
            
            # اطبع W بدون نزول سطر (مع مسافة)
            print("W", end=" ")
        
        else:
            # اطبع B بدون نزول سطر (مع مسافة)
            print("B", end=" ")

    # بعد ما نخلص صف كامل، ننزل سطر جديد
    print("\n")
